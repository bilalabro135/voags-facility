    public function approve($id)
    {
        $invoice = Invoice::find($id);
        $invoice->status = 'Approved';
        $invoice->save();
        $notification = array(
            'message' => 'Invoice Approved',
            'alert-type' => 'success'
        );
       return redirect()->back()->with($notification);
    }
    public function decline($id)
    {
